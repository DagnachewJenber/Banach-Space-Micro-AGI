# 2. Centralized Global MeTTa Engine
# ==========================================
@st.cache_resource
def get_global_metta_engine():
    metta = MeTTa()
    metta.run("""
        ;; Base Structural Ontology
        (: VectorSpace Type)
        (: BanachSpace Type)
        (: Complete Property)
        (: Normed Property)

        ;; Fundamental Rules
        (= (is-banach $space) (and (has-property $space Normed) (has-property $space Complete)))
        
        ;; Peer-Reviewed Core Truths
        (: L2 VectorSpace)
        (has-property L2 Normed)
        (has-property L2 Complete)
    """)
    return metta


global_metta = get_global_metta_engine()
client = OpenAI(
    base_url="https://openrouter.ai/api/v1",
    api_key=st.secrets["OPENROUTER_API_KEY"],
)


# Helper tools pointing to the global instance
def run_global_metta(query_string):
    try:
        res = global_metta.run(query_string)
        return str(res)
    except Exception as e:
        return f"MeTTa Error: {str(e)}"


def run_local_sympy(code_string):
    output_buffer = io.StringIO()
    local_env = {"sp": sp, "sympy": sp}
    try:
        with contextlib.redirect_stdout(output_buffer):
            exec(code_string, {}, local_env)
        return output_buffer.getvalue()
    except Exception as e:
        return f"SymPy Error: {str(e)}"


# ==========================================
# 3. Text Post-Processing (Robust Math Render)
# ==========================================
def clean_and_format_latex(text):
    """Normalizes conflicting LLM LaTeX and Markdown configurations

    for flawless, presentation-grade math rendering in Streamlit.
    """
    if not text:
        return ""

    # Convert standard block delimiters
    text = text.replace(r"\[", "\n$$\n").replace(r"\]", "\n$$\n")
    text = text.replace(r"\(", " $ ").replace(r"\)", " $ ")

    # Standardize LaTeX environments often generated by technical reasoners
    text = re.sub(r"\\begin\{equation\*?\}(.*?)\\end\{equation\*?\}", r"\n$$\1$$\n", text, flags=re.DOTALL)
    text = re.sub(r"\\begin\{align\*?\}(.*?)\\end\{align\*?\}", r"\n$$\1$$\n", text, flags=re.DOTALL)

    # Protect double backslashes in alignment arrays from python string escaping
    text = text.replace(r"\\", r"\\\\")

    # Clean double spacing or artifact boundaries around inline markers
    text = text.replace("$$ $$", "$$")
    return text



MODEL_TARGET = "openrouter/free"

AGI_SYSTEM_PROMPT = (
    "You are an autonomous Micro-AGI Agent executing inside a global Neuro-Symbolic cognitive loop.\n"
    "Your specialization is Banach Space Theory and Functional Analysis.\n\n"
    "CRITICAL STRUCTURAL RULE:\n"
    "You must think step-by-step. To execute logic check or mathematical calculation, output a single JSON block.\n"
    "Do NOT output content or text surrounding the JSON when calling tools.\n\n"
    "COMMAND STRUCTURE:\n"
    "To evaluate logic, output a JSON block:\n"
    '{\n  "action": "tool_call",\n  "tool": "metta_eval",\n  "input": "!(is-banach L2)"\n}\n\n'
    "To commit a verified truth to global mind, output:\n"
    '{\n  "action": "tool_call",\n  "tool": "metta_publish",\n  "input": "(has-property YourNewSpace Complete)"\n}\n\n'
    "To deliver your final verified answer with rich mathematical proofs, output:\n"
    '{\n  "action": "final_answer",\n  "content": "Your complete math proof string using standard LaTeX."\n}'
)

if "chat_history" not in st.session_state:
    st.session_state.chat_history = []

# ==========================================
# 5. Interface Layout
# ==========================================
col_main, col_global_vault = st.columns([2, 1])

with col_main:
    st.subheader("🤖 Collaborative Thought Loop")

    for msg in st.session_state.chat_history:
        with st.chat_message(msg["role"]):
            st.markdown(msg["content"])

    if user_input := st.chat_input("Enter research hypothesis..."):
        with st.chat_message("user"):
            st.markdown(user_input)
        st.session_state.chat_history.append(
            {"role": "user", "content": user_input}
        )

        with st.chat_message("assistant"):
            thought_container = st.status(
                "🧠 Consultation with Global Knowledge Base...", expanded=True
            )
            loop_messages = [
                {"role": "system", "content": AGI_SYSTEM_PROMPT},
                {"role": "user", "content": user_input},
            ]

            max_cycles = 5
            cycle = 0
            final_output = ""

            while cycle < max_cycles:
                cycle += 1
                raw_step = ""

                try:
                    response = client.chat.completions.create(
                        model=MODEL_TARGET, 
                        messages=loop_messages,
                        temperature=0.1
                    )
                    
                    # FIX: Defensive check to completely protect against NoneType output streams
                    message_content = response.choices[0].message.content
                    if message_content is not None:
                        raw_step = message_content.strip()
                    else:
                        raw_step = ""

                    # Clean potential markdown wrapping around JSON strings
                    json_clean = re.sub(r"^```json|```$", "", raw_step, flags=re.IGNORECASE).strip()
                    command = json.loads(json_clean)
                except Exception as e:
                    if not raw_step:
                        # Fallback step if the routing layer drops connectivity entirely
                        command = {
                            "action": "final_answer",
                            "content": "The routing engine encountered an empty stream frame response. Please submit your hypothesis query again to re-engage the loop.",
                        }
                    else:
                        # Fallback if text is outputted directly without explicit JSON wrapper logic
                        command = {"action": "final_answer", "content": raw_step}

                if command.get("action") == "tool_call":
                    tool_name = command.get("tool")
                    tool_input = command.get("input")

                    if tool_name == "metta_eval":
                        observation = run_global_metta(tool_input)
                    elif tool_name == "metta_publish":
                        observation = run_global_metta(tool_input)
                        observation = "COMMITTED TO GLOBAL KNOWLEDGE ACCESSIBLE BY ALL USERS."
                    elif tool_name == "sympy_calc":
                        observation = run_local_sympy(tool_input)
                    else:
                        observation = "Error: Invalid tool connection choice."

                    loop_messages.append({"role": "assistant", "content": raw_step})
                    loop_messages.append(
                        {
                            "role": "system",
                            "content": f"Observation: {observation}",
                        }
                    )

                elif command.get("action") == "final_answer":
                    final_output = command.get("content")
                    break

            # Clean and normalize mathematical markdown blocks
            final_output = clean_and_format_latex(final_output)

            thought_container.update(
                label="✅ Output Compiled!", state="complete", expanded=False
            )
            
            if final_output:
                st.markdown(final_output)
                st.session_state.chat_history.append(
                    {"role": "assistant", "content": final_output}
                )

with col_global_vault:
    st.subheader("🏛️ Shared Peer-Reviewed Vault")
    st.write(
        "Logical assertions currently verified common to **all** global users."
    )

